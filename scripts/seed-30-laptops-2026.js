async function seedLaptops() {
  try {
    console.log(`📱 Seeding ${laptops.length} laptops into Supabase...`);

    // Insert in batches of 5 to avoid request timeouts
    const batchSize = 5;
    let inserted = 0;

    for (let i = 0; i < laptops.length; i += batchSize) {
      const batch = laptops.slice(i, i + batchSize);
      const { data, error } = await supabase.from('laptops').insert(batch);

      if (error) {
        console.error(`❌ Batch ${Math.floor(i / batchSize) + 1} failed:`, error);
      } else {
        inserted += batch.length;
        console.log(`✅ Inserted batch ${Math.floor(i / batchSize) + 1}: ${batch.length} laptops (${inserted}/${laptops.length})`);
      }
    }

    console.log(`\n✨ Seeding complete! Total inserted: ${inserted}/${laptops.length}`);

    // Verify
    const { count, error: countError } = await supabase
      .from('laptops')
      .select('*', { count: 'exact' });

    if (countError) {
      console.error('Failed to count laptops:', countError);
    } else {
      console.log(`\n📊 Database now has ${count} total active laptops`);
    }
  } catch (err) {
    console.error('Fatal error during seeding:', err);
    process.exit(1);
  }
}

seedLaptops();
