async function main() {
  console.log(`\n🚀 Starting upsert of ${laptops.length} laptops...\n`);

  const BATCH_SIZE = 10;
  let totalUpserted = 0;

  for (let i = 0; i < laptops.length; i += BATCH_SIZE) {
    const batch = laptops.slice(i, i + BATCH_SIZE);
    const batchNum = Math.floor(i / BATCH_SIZE) + 1;

    console.log(`📦 Batch ${batchNum}: Upserting ${batch.map(l => l.name).join(', ')}...`);

    const { data, error } = await supabase
      .from('laptops')
      .upsert(batch, { onConflict: 'slug' })
      .select('id, name, slug');

    if (error) {
      console.error(`❌ Batch ${batchNum} error:`, error.message);
      console.error('   Details:', JSON.stringify(error, null, 2));
    } else {
      console.log(`✅ Batch ${batchNum} done — ${data.length} laptops upserted`);
      data.forEach(l => console.log(`   • ${l.name} (${l.slug})`));
      totalUpserted += data.length;
    }

    // Small delay between batches
    if (i + BATCH_SIZE < laptops.length) {
      await new Promise(r => setTimeout(r, 300));
    }
  }

  console.log(`\n📊 Summary: ${totalUpserted}/${laptops.length} laptops upserted successfully`);

  // Verify total count
  const { count } = await supabase
    .from('laptops')
    .select('*', { count: 'exact', head: true })
    .eq('is_active', true);

  console.log(`📋 Total active laptops in Supabase: ${count}`);
  console.log('\n✨ Done! Run generate-explanations to pre-cache descriptions for new laptops.');
  console.log('   curl -X POST http://localhost:3000/api/admin/generate-explanations -H "x-admin-key: laptop-advisor-admin-2026" -d "{}"\n');
}

main().catch(err => {
  console.error('❌ Fatal error:', err);
  process.exit(1);
});
